package com.mustafa.roman.services;

public class RomanNumber {

	int number;
	String roman;

	public RomanNumber(int number, String roman) {
		super();
		this.number = number;
		this.roman = roman;
	}

	public int getNumber() {
		return number;
	}

	public String getRoman() {
		return roman;
	}

}
